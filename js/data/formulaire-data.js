function buildProfile(candidate) {
  const details = [
    { label: 'Nom complet', value: candidate.fullName },
    { label: 'Date de naissance', value: candidate.birthDate },
    { label: 'Adresse', value: candidate.address },
    { label: 'Ville', value: candidate.city },
    { label: 'E-mail', value: candidate.email },
    { label: 'Telephone', value: candidate.phone },
    { label: 'Nationalite', value: candidate.nationality },
    { label: 'Permis de travail', value: candidate.workPermit },
    { label: 'Disponibilite', value: candidate.availability },
    { label: 'Taux souhaite', value: candidate.workRate },
    { label: 'Salaire souhaite', value: candidate.salary },
    { label: 'Langues', value: candidate.languages },
    { label: 'Experience', value: candidate.experience },
    { label: 'Motivation', value: candidate.motivation }
  ];

  const fields = [
    { key: 'fullName', label: 'Nom et prenom', type: 'text', value: candidate.fullName, placeholder: 'Nom complet' },
    { key: 'birthDate', label: 'Date de naissance', type: 'date', value: candidate.birthDate },
    { key: 'address', label: 'Adresse complete', type: 'text', value: candidate.address, placeholder: 'Rue, numero, NPA, ville' },
    { key: 'city', label: 'Ville', type: 'text', value: candidate.city, placeholder: 'Ville de residence' },
    { key: 'email', label: 'E-mail', type: 'email', value: candidate.email, placeholder: 'nom@domaine.ch' },
    { key: 'phone', label: 'Telephone', type: 'tel', value: candidate.phone, placeholder: '+41 xx xxx xx xx' },
    { key: 'nationality', label: 'Nationalite', type: 'select', value: candidate.nationality, options: FORM_OPTIONS.nationality },
    { key: 'workPermit', label: 'Permis de travail', type: 'select', value: candidate.workPermit, options: FORM_OPTIONS.workPermit },
    { key: 'availability', label: 'Disponibilite', type: 'select', value: candidate.availability, options: FORM_OPTIONS.availability },
    { key: 'workRate', label: 'Taux souhaite', type: 'select', value: candidate.workRate, options: FORM_OPTIONS.workRate },
    { key: 'salary', label: 'Salaire souhaite', type: 'text', value: candidate.salary, placeholder: 'Ex: 4500 CHF brut' },
    { key: 'languages', label: 'Langues', type: 'text', value: candidate.languages, placeholder: 'Ex: Francais, Anglais B1' },
    { key: 'experience', label: 'Resume experience', type: 'textarea', value: candidate.experience, placeholder: 'Experience principale' },
    { key: 'motivation', label: 'Message de motivation', type: 'textarea', value: candidate.motivation, placeholder: 'Motivation courte' }
  ];

  return {
    category: candidate.category,
    profileTitle: candidate.profileTitle,
    profileIntro: candidate.profileIntro,
    profileDetails: details,
    formTitle: candidate.formTitle,
    formIntro: 'Saisissez toutes les informations comme dans un vrai portail de candidature.',
    fields
  };
}

